## Calculadora

# Inserir valores
valor_1 = int(input('Informe um número: '))
valor_2 = int(input('Informe um número: '))

# Cálculos

print(f'Soma de {valor_1} + {valor_2} = {valor_1 + valor_2}')
print(f'Subtração de {valor_1} - {valor_2} = {valor_1 - valor_2}')
print(f'Multiplicação de {valor_1} x {valor_2} = {valor_1 * valor_2}')
print(f'Divisão de {valor_1} / {valor_2} = {valor_1 / valor_2}')
print(f'Resto de {valor_1} % {valor_2} = {valor_1 % valor_2}')